public class CubeStudent21 {
    static int area(int x, int y) {
        return x * y;
    }

    static int volume(int x, int y, int z) {
        return x * y * z;
    }

    public static void main(String[] args) {
        System.out.println(area(6, 12));
        System.out.println(volume(5, 10, 15));
    }
}
